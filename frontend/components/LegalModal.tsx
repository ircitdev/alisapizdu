export default function LegalModal({ isOpen, onClose, type }: LegalModalProps) {
  const overlayRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleKey = (e: KeyboardEvent) => {
      if (e.key === 'Escape' && isOpen) onClose();
    };
    window.addEventListener('keydown', handleKey);
    return () => window.removeEventListener('keydown', handleKey);
  }, [isOpen, onClose]);

  const handleOverlayClick = useCallback(
    (e: React.MouseEvent) => {
      if (e.target === overlayRef.current) onClose();
    },
    [onClose]
  );

  if (!isOpen) return null;

  const content = CONTENT[type];

  return (
    <div
      ref={overlayRef}
      onClick={handleOverlayClick}
      className="fixed inset-0 z-50 flex items-center justify-center
                 bg-black/70 backdrop-blur-sm animate-fade-in px-4"
    >
      <div className="w-full max-w-lg max-h-[80vh] mx-auto bg-bg-card rounded-2xl border border-white/10 flex flex-col animate-slide-in">
        <div className="flex items-center justify-between p-5 pb-3 border-b border-white/5">
          <h2 className="text-base font-bold text-white">{content.title}</h2>
          <button onClick={onClose} className="text-white/40 hover:text-white/70 transition-colors p-1">
            <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
              <path d="M18 6L6 18M6 6l12 12" />
            </svg>
          </button>
        </div>
        <div className="overflow-y-auto p-5 pt-3">
          <pre className="text-white/50 text-xs leading-relaxed whitespace-pre-wrap font-[inherit]">
            {content.text}
          </pre>
        </div>
      </div>
    </div>
  );
}
